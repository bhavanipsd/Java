package basics;

public class swap {
	public static void main(String[] args)
	{
		int n1=10,n2=90,n3=0,n4=10,n5=90;
		n3=n1;
		n1=n2;
		n2=n3;
		System.out.println(n1+" "+n2+" ");
		//alternate without third variable
		
		n4=n4+n5;
		n5=n4-n5;
		n4=n4-n5;
		System.out.println(n4+"  "+n5+" ");
		
		//swapping of three numbers
		
	}
}
